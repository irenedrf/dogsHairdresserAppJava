    public void buscarPiensoAdecuado(){
        try {
            
            con =  DriverManager.getConnection("jdbc:mysql://localhost:3306/proyecto_final_irene?&user=root&password=papalumala31"
                           + "&useLegacyDatetimeCode=false&serverTimezone=UTC");  
            
            String sql= "SELECT raza, piensoAdecuado FROM razasperros WHERE nombreMascota='"+campoNombreMascota.getText()+"'";
            
            Statement stmt = (Statement) con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_READ_ONLY); 
            rs = stmt.executeQuery(sql);
            
            while (rs.next()){
                etiquetaTuRaza.setText("La raza de tu perro es "+rs.getString("raza")+".");
                etiquetaTuPienso.setText("El tipo de alimento más adecuado es "+rs.getString("piensoAdecuado")+".");
            }
 		
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "No tenemos registrado el nombre de su mascota. Compruebe la ortografía.", "WARNING MESSAGE", JOptionPane.WARNING_MESSAGE);
        }
        
        
        
        
    }
    
    
    
