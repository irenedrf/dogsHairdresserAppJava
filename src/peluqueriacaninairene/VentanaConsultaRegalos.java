    public void calcularRegalo(){
        String importe=importeGastado.getText();
        double impo=Double.valueOf(importe);
        
        if (impo>=50) {
            
            if (impo>=50 && impo<100) {
                mostrarRegalo.setText("Una bolsa de chuches para tu perro.");
            }
            if (impo>=100 && impo<150) {
                mostrarRegalo.setText("Una bolsa de pienso premium");
            }
            if (impo>=150 && impo<200) {
                mostrarRegalo.setText("Un baño y corte de pelo y de uñas para tu perro.");
            }
            if (impo>=200) {
                mostrarRegalo.setText("Tres bolsas de pienso premium y un baño gratis.");
            }
        } else {
                mostrarRegalo.setText(":( No llegas al importe mínimo para regalo.");
        }
        
    }
    
    
